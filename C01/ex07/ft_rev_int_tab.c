void	ft_rev_int_tab(int *tab, int size)
{
	int	*l;
	int	*r;
	int	temp;

	r = tab;
	l = tab + size - 1;
	while (r < l)
	{
		temp = *r;
		*r = *l;
		*l = temp;
		r++;
		l--;
	}
}
